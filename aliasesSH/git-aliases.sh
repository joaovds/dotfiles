#!/bin/bash

alias gaa 'git add --all'
alias gst 'git status'
alias gcm 'git commit -m'
alias gco 'git checkout'
alias gbr 'git branch'
alias gpl 'git pull'
alias gps 'git push'
alias glg 'git log --oneline --decorate --color'
alias gck 'git checkout'

function ghelp
    echo "Git Aliases:"
    echo "gaa - git add --all"
    echo "gst - git status"
    echo "gcm - git commit -m"
    echo "gco - git checkout"
    echo "gbr - git branch"
    echo "gpl - git pull"
    echo "gps - git push"
    echo "glg - git log --oneline --decorate --color"
    echo "gck - git checkout"
end

